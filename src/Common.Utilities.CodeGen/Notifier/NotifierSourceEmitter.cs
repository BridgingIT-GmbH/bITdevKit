namespace BridgingIT.DevKit.Common;

using System.Text;

/// <summary>
/// Emits C# source for validated notifier generation models.
/// </summary>
public static class NotifierSourceEmitter
{
    /// <summary>
    /// Emits the generated source for a validated notifier model.
    /// </summary>
    /// <param name="model">The validated notifier generation model.</param>
    /// <returns>The generated C# source.</returns>
    public static string Emit(NotifierGenerationModel model)
    {
        var builder = new StringBuilder();
        builder.AppendLine("// <auto-generated />");
        builder.AppendLine("// source: NotifierSourceGenerator");
        builder.AppendLine("using FluentValidation;");
        builder.AppendLine();

        if (!string.IsNullOrWhiteSpace(model.NamespaceName))
        {
            builder.Append("namespace ").Append(model.NamespaceName).AppendLine();
            builder.AppendLine("{");
        }

        EmitEventPartial(builder, model);

        foreach (var handleMethod in model.HandleMethods)
        {
            builder.AppendLine();
            EmitHandler(builder, model, handleMethod);
        }

        if (!string.IsNullOrWhiteSpace(model.NamespaceName))
        {
            builder.AppendLine("}");
        }

        return builder.ToString();
    }

    private static void EmitEventPartial(StringBuilder builder, NotifierGenerationModel model)
    {
        var eventTypeName = RequesterGeneratorSymbolHelper.GetTypeName(model.ClassSymbol);

        builder.Append("    ").Append(model.AccessibilityKeyword).Append(" partial class ").Append(model.ClassSymbol.Name);
        if (model.EmitNotificationBase)
        {
            builder.Append(" : global::BridgingIT.DevKit.Common.NotificationBase");
        }

        builder.AppendLine();
        builder.AppendLine("    {");

        foreach (var handleMethod in model.HandleMethods)
        {
            EmitBridgeMethod(builder, eventTypeName, handleMethod);
            builder.AppendLine();
        }

        EmitHelpers(builder, "private", "generated event authoring surface");

        if (model.HasValidation)
        {
            builder.AppendLine();
            EmitValidator(builder, model, eventTypeName);
        }

        builder.AppendLine("    }");
    }

    private static void EmitBridgeMethod(StringBuilder builder, string eventTypeName, NotifierHandleMethodModel handleMethod)
    {
        builder.AppendLine("        /// <summary>");
        builder.AppendLine("        /// Invokes the developer-authored <see cref=\"global::BridgingIT.DevKit.Common.HandleAttribute\"/> method for the generated Notifier plumbing.");
        builder.AppendLine("        /// </summary>");
        builder.AppendLine("        /// <param name=\"notification\">The notification instance supplied by the Notifier runtime.</param>");
        builder.AppendLine("        /// <param name=\"options\">The publish options supplied by the Notifier runtime.</param>");
        builder.AppendLine("        /// <param name=\"cancellationToken\">The cancellation token supplied by the Notifier runtime.</param>");

        foreach (var parameter in handleMethod.Parameters.Where(static parameter => parameter.Kind == NotifierParameterKind.Service))
        {
            builder.Append("        /// <param name=\"")
                .Append(RequesterGeneratorSymbolHelper.EscapeIdentifier(parameter.Symbol.Name))
                .AppendLine("\">The service dependency resolved for the generated handler invocation.</param>");
        }

        builder.AppendLine("        /// <returns>The result returned by the developer-authored handle method.</returns>");
        builder.Append("        internal static global::System.Threading.Tasks.Task<global::BridgingIT.DevKit.Common.Result> ")
            .Append(handleMethod.BridgeMethodName)
            .Append("(")
            .Append(eventTypeName)
            .Append(" notification, global::BridgingIT.DevKit.Common.PublishOptions options, global::System.Threading.CancellationToken cancellationToken");

        foreach (var parameter in handleMethod.Parameters.Where(static parameter => parameter.Kind == NotifierParameterKind.Service))
        {
            builder.Append(", ")
                .Append(RequesterGeneratorSymbolHelper.GetTypeName(parameter.Symbol.Type))
                .Append(' ')
                .Append(RequesterGeneratorSymbolHelper.EscapeIdentifier(parameter.Symbol.Name));
        }

        builder.AppendLine(")");
        builder.AppendLine("        {");
        builder.Append("            ");
        if (!handleMethod.ReturnsTask)
        {
            builder.Append("return global::System.Threading.Tasks.Task.FromResult(");
        }
        else
        {
            builder.Append("return ");
        }

        builder.Append("notification.")
            .Append(handleMethod.MethodSymbol.Name)
            .Append('(')
            .Append(string.Join(", ", handleMethod.Parameters.Select(static parameter => parameter.Kind switch
            {
                NotifierParameterKind.PublishOptions => "options",
                NotifierParameterKind.CancellationToken => "cancellationToken",
                NotifierParameterKind.Service => RequesterGeneratorSymbolHelper.EscapeIdentifier(parameter.Symbol.Name),
                _ => throw new InvalidOperationException(),
            })))
            .Append(')');

        if (!handleMethod.ReturnsTask)
        {
            builder.Append(')');
        }

        builder.AppendLine(";");
        builder.AppendLine("        }");
    }

    private static void EmitHandler(StringBuilder builder, NotifierGenerationModel model, NotifierHandleMethodModel handleMethod)
    {
        var eventTypeName = RequesterGeneratorSymbolHelper.GetTypeName(model.ClassSymbol);

        foreach (var attribute in model.PolicyAttributes)
        {
            builder.Append("    ").AppendLine(RequesterGeneratorSymbolHelper.RenderAttribute(attribute));
        }

        builder.AppendLine("    /// <summary>");
        builder.Append("    /// Represents the generated Notifier handler for <see cref=\"")
            .Append(eventTypeName)
            .Append("\"/> and authored handle method <c>")
            .Append(handleMethod.MethodSymbol.Name)
            .AppendLine("</c>.");
        builder.AppendLine("    /// </summary>");
        builder.Append("    ").Append(model.AccessibilityKeyword).Append(" class ").Append(handleMethod.GeneratedHandlerName)
            .Append(" : global::BridgingIT.DevKit.Common.NotificationHandlerBase<")
            .Append(eventTypeName)
            .AppendLine(">");
        builder.AppendLine("    {");
        builder.AppendLine("        private readonly global::System.IServiceProvider serviceProvider;");
        builder.AppendLine();
        builder.AppendLine("        /// <summary>");
        builder.Append("        /// Initializes a new instance of the <see cref=\"")
            .Append(handleMethod.GeneratedHandlerName)
            .AppendLine("\"/> class.");
        builder.AppendLine("        /// </summary>");
        builder.AppendLine("        /// <param name=\"serviceProvider\">The service provider used to resolve dependencies for the generated handler invocation.</param>");
        builder.Append("        public ").Append(handleMethod.GeneratedHandlerName).AppendLine("(global::System.IServiceProvider serviceProvider)");
        builder.AppendLine("        {");
        builder.AppendLine("            this.serviceProvider = serviceProvider ?? throw new global::System.ArgumentNullException(nameof(serviceProvider));");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        /// <summary>");
        builder.Append("        /// Forwards the Notifier runtime invocation for <see cref=\"")
            .Append(eventTypeName)
            .AppendLine("\"/> into the developer-authored handle method.");
        builder.AppendLine("        /// </summary>");
        builder.AppendLine("        /// <param name=\"notification\">The notification to handle.</param>");
        builder.AppendLine("        /// <param name=\"options\">The notification publish options.</param>");
        builder.AppendLine("        /// <param name=\"cancellationToken\">The cancellation token for the notification.</param>");
        builder.AppendLine("        /// <returns>The result returned by the developer-authored handle method.</returns>");
        builder.Append("        protected override global::System.Threading.Tasks.Task<global::BridgingIT.DevKit.Common.Result> HandleAsync(")
            .Append(eventTypeName)
            .AppendLine(" notification, global::BridgingIT.DevKit.Common.PublishOptions options, global::System.Threading.CancellationToken cancellationToken)");
        builder.AppendLine("        {");

        foreach (var parameter in handleMethod.Parameters.Where(static parameter => parameter.Kind == NotifierParameterKind.Service))
        {
            var parameterName = RequesterGeneratorSymbolHelper.EscapeIdentifier(parameter.Symbol.Name);
            builder.Append("            var ")
                .Append(parameterName)
                .Append(" = global::Microsoft.Extensions.DependencyInjection.ServiceProviderServiceExtensions.GetRequiredService<")
                .Append(RequesterGeneratorSymbolHelper.GetTypeName(parameter.Symbol.Type))
                .Append(">(this.serviceProvider);")
                .AppendLine();
        }

        builder.Append("            return ")
            .Append(eventTypeName)
            .Append('.')
            .Append(handleMethod.BridgeMethodName)
            .Append("(notification, options, cancellationToken");

        foreach (var parameter in handleMethod.Parameters.Where(static parameter => parameter.Kind == NotifierParameterKind.Service))
        {
            builder.Append(", ").Append(RequesterGeneratorSymbolHelper.EscapeIdentifier(parameter.Symbol.Name));
        }

        builder.AppendLine(");");
        builder.AppendLine("        }");
        builder.AppendLine("    }");
    }

    private static void EmitValidator(StringBuilder builder, NotifierGenerationModel model, string eventTypeName)
    {
        builder.AppendLine("        /// <summary>");
        builder.Append("        /// Provides generated FluentValidation integration for <see cref=\"")
            .Append(eventTypeName)
            .AppendLine("\"/>.");
        builder.AppendLine("        /// </summary>");
        builder.AppendLine("        public sealed class Validator : global::FluentValidation.AbstractValidator<" + eventTypeName + ">");
        builder.AppendLine("        {");
        builder.AppendLine("            /// <summary>");
        builder.Append("            /// Initializes a new instance of the <see cref=\"")
            .Append(eventTypeName)
            .AppendLine(".Validator\"/> class.");
        builder.AppendLine("            /// </summary>");
        builder.AppendLine("            public Validator()");
        builder.AppendLine("            {");

        if (model.PropertyValidationRules.Length > 0)
        {
            ValidationSourceEmitter.EmitRules(builder, model.PropertyValidationRules, "                ");
        }

        if (model.ValidateMethod is not null)
        {
            builder.Append("                var validator = new global::FluentValidation.InlineValidator<")
                .Append(eventTypeName)
                .AppendLine(">();");
            builder.Append("                ").Append(eventTypeName).Append('.').Append(model.ValidateMethod.Name).AppendLine("(validator);");
            builder.AppendLine("                this.Include(validator);");
        }

        builder.AppendLine("            }");
        builder.AppendLine("        }");
    }

    private static void EmitHelpers(StringBuilder builder, string accessibility, string surface)
    {
        EmitHelper(builder, accessibility, "global::BridgingIT.DevKit.Common.Result", "Success", string.Empty, "global::BridgingIT.DevKit.Common.Result.Success()", $"Creates a successful result for the {surface}.");
        EmitHelper(builder, accessibility, "global::BridgingIT.DevKit.Common.Result", "Failure", string.Empty, "global::BridgingIT.DevKit.Common.Result.Failure()", $"Creates a failed result without any additional message or error for the {surface}.");
        EmitHelper(builder, accessibility, "global::BridgingIT.DevKit.Common.Result", "Failure", "string message", "global::BridgingIT.DevKit.Common.Result.Failure(message)", $"Creates a failed result with a message for the {surface}.");
        EmitHelper(builder, accessibility, "global::BridgingIT.DevKit.Common.Result", "Failure", "global::BridgingIT.DevKit.Common.IResultError error", "global::BridgingIT.DevKit.Common.Result.Failure(error)", $"Creates a failed result with a typed error for the {surface}.");
    }

    private static void EmitHelper(
        StringBuilder builder,
        string accessibility,
        string returnType,
        string methodName,
        string parameters,
        string body,
        string summary)
    {
        builder.AppendLine("        /// <summary>");
        builder.Append("        /// ").AppendLine(summary);
        builder.AppendLine("        /// </summary>");

        if (!string.IsNullOrWhiteSpace(parameters))
        {
            foreach (var parameter in parameters.Split(',').Select(static parameter => parameter.Trim()))
            {
                var parameterName = parameter.Split(' ').Last();
                builder.Append("        /// <param name=\"").Append(parameterName).AppendLine("\">The value used to create the generated result.</param>");
            }
        }

        builder.AppendLine("        /// <returns>The generated Notifier result.</returns>");
        builder.Append("        ")
            .Append(accessibility)
            .Append(" static ")
            .Append(returnType)
            .Append(' ')
            .Append(methodName)
            .Append('(')
            .Append(parameters)
            .Append(") => ")
            .Append(body)
            .AppendLine(";");
        builder.AppendLine();
    }
}
