namespace BridgingIT.DevKit.Common;

using System.Text;

/// <summary>
/// Emits C# source for validated pipeline-context validation generation models.
/// </summary>
public static class PipelineContextValidationSourceEmitter
{
    /// <summary>
    /// Emits the generated source for a validated pipeline-context validation model.
    /// </summary>
    /// <param name="model">The validated generation model.</param>
    /// <returns>The generated C# source.</returns>
    public static string Emit(PipelineContextValidationGenerationModel model)
    {
        var builder = new StringBuilder();
        builder.AppendLine("// <auto-generated />");
        builder.AppendLine("// source: PipelineContextValidationSourceGenerator");
        builder.AppendLine("using FluentValidation;");
        builder.AppendLine();

        if (!string.IsNullOrWhiteSpace(model.NamespaceName))
        {
            builder.Append("namespace ").Append(model.NamespaceName).AppendLine();
            builder.AppendLine("{");
        }

        EmitContextPartial(builder, model);

        if (!string.IsNullOrWhiteSpace(model.NamespaceName))
        {
            builder.AppendLine("}");
        }

        return builder.ToString();
    }

    private static void EmitContextPartial(StringBuilder builder, PipelineContextValidationGenerationModel model)
    {
        var contextTypeName = RequesterGeneratorSymbolHelper.GetTypeName(model.ClassSymbol);

        builder.Append("    ")
            .Append(model.AccessibilityKeyword);

        if (model.IsSealed)
        {
            builder.Append(" sealed");
        }

        builder.Append(" partial class ")
            .Append(model.ClassSymbol.Name)
            .AppendLine();
        builder.AppendLine("    {");
        builder.AppendLine("        /// <summary>");
        builder.Append("        /// Provides generated FluentValidation integration for <see cref=\"")
            .Append(contextTypeName)
            .AppendLine("\"/>.");
        builder.AppendLine("        /// </summary>");
        builder.AppendLine("        public sealed class Validator : global::FluentValidation.AbstractValidator<" + contextTypeName + ">");
        builder.AppendLine("        {");
        builder.AppendLine("            /// <summary>");
        builder.Append("            /// Initializes a new instance of the <see cref=\"")
            .Append(contextTypeName)
            .AppendLine(".Validator\"/> class.");
        builder.AppendLine("            /// </summary>");
        builder.AppendLine("            public Validator()");
        builder.AppendLine("            {");

        if (model.PropertyValidationRules.Length > 0)
        {
            // Property validation attributes are emitted as regular FluentValidation rule chains so
            // pipeline contexts use the same FluentValidation execution model as Requester requests.
            ValidationSourceEmitter.EmitRules(builder, model.PropertyValidationRules, "                ");
        }

        if (model.ValidateMethod is not null)
        {
            builder.Append("                var validator = new global::FluentValidation.InlineValidator<")
                .Append(contextTypeName)
                .AppendLine(">();");
            builder.Append("                ").Append(contextTypeName).Append('.').Append(model.ValidateMethod.Name).AppendLine("(validator);");
            builder.AppendLine("                this.Include(validator);");
        }

        builder.AppendLine("            }");
        builder.AppendLine("        }");
        builder.AppendLine("    }");
    }
}
