namespace BridgingIT.DevKit.Common;

using System.Text;

/// <summary>
/// Emits C# source for validated Requester generation models.
/// </summary>
public static class RequesterSourceEmitter
{
    /// <summary>
    /// Emits the generated source for a validated Requester model.
    /// </summary>
    /// <param name="model">The validated Requester generation model.</param>
    /// <returns>The generated C# source.</returns>
    public static string Emit(RequesterGenerationModel model)
    {
        var builder = new StringBuilder();
        builder.AppendLine("// <auto-generated />");
        builder.AppendLine("// source: RequesterSourceGenerator");

        if (!string.IsNullOrWhiteSpace(model.NamespaceName))
        {
            builder.Append("namespace ").Append(model.NamespaceName).AppendLine();
            builder.AppendLine("{");
        }

        EmitRequestPartial(builder, model);
        builder.AppendLine();
        EmitHandler(builder, model);

        if (!string.IsNullOrWhiteSpace(model.NamespaceName))
        {
            builder.AppendLine("}");
        }

        return builder.ToString();
    }

    private static void EmitRequestPartial(StringBuilder builder, RequesterGenerationModel model)
    {
        var requestTypeName = RequesterGeneratorSymbolHelper.GetTypeName(model.ClassSymbol);
        var responseTypeName = RequesterGeneratorSymbolHelper.GetTypeName(model.ResponseType);
        var resultTypeName = $"global::BridgingIT.DevKit.Common.Result<{responseTypeName}>";

        builder.Append("    ").Append(model.AccessibilityKeyword).Append(" partial class ").Append(model.ClassSymbol.Name);
        if (model.EmitRequestBase)
        {
            builder.Append(" : global::BridgingIT.DevKit.Common.RequestBase<").Append(responseTypeName).Append('>');
        }

        builder.AppendLine();
        builder.AppendLine("    {");
        builder.AppendLine("        /// <summary>");
        builder.AppendLine("        /// Invokes the developer-authored <see cref=\"global::BridgingIT.DevKit.Common.HandleAttribute\"/> method for the generated Requester plumbing.");
        builder.AppendLine("        /// </summary>");
        builder.AppendLine("        /// <param name=\"request\">The request instance supplied by the Requester runtime.</param>");
        builder.AppendLine("        /// <param name=\"options\">The send options supplied by the Requester runtime.</param>");
        builder.AppendLine("        /// <param name=\"cancellationToken\">The cancellation token supplied by the Requester runtime.</param>");

        foreach (var parameter in model.HandleMethod.Parameters.Where(static parameter => parameter.Kind == RequesterParameterKind.Service))
        {
            builder.Append("        /// <param name=\"")
                .Append(RequesterGeneratorSymbolHelper.EscapeIdentifier(parameter.Symbol.Name))
                .AppendLine("\">The service dependency resolved for the generated handler invocation.</param>");
        }

        builder.AppendLine("        /// <returns>The result returned by the developer-authored handle method.</returns>");
        builder.Append("        internal static global::System.Threading.Tasks.Task<")
            .Append(resultTypeName)
            .Append("> ")
            .Append(RequesterGenerationModel.HandleBridgeMethodName)
            .Append("(")
            .Append(requestTypeName)
            .Append(" request, global::BridgingIT.DevKit.Common.SendOptions options, global::System.Threading.CancellationToken cancellationToken");

        foreach (var parameter in model.HandleMethod.Parameters.Where(static parameter => parameter.Kind == RequesterParameterKind.Service))
        {
            builder.Append(", ")
                .Append(RequesterGeneratorSymbolHelper.GetTypeName(parameter.Symbol.Type))
                .Append(' ')
                .Append(RequesterGeneratorSymbolHelper.EscapeIdentifier(parameter.Symbol.Name));
        }

        builder.AppendLine(")");
        builder.AppendLine("        {");

        builder.Append("            ");
        if (!model.HandleMethod.ReturnsTask)
        {
            builder.Append("return global::System.Threading.Tasks.Task.FromResult(");
        }
        else
        {
            builder.Append("return ");
        }

        // The request partial owns the bridge because that keeps the authored handle method close to
        // the generated convenience helpers while still letting the runtime handler stay framework-shaped.
        builder.Append("request.")
            .Append(model.HandleMethod.MethodSymbol.Name)
            .Append('(')
            .Append(string.Join(", ", model.HandleMethod.Parameters.Select(static parameter => parameter.Kind switch
            {
                RequesterParameterKind.SendOptions => "options",
                RequesterParameterKind.CancellationToken => "cancellationToken",
                RequesterParameterKind.Service => RequesterGeneratorSymbolHelper.EscapeIdentifier(parameter.Symbol.Name),
                _ => throw new InvalidOperationException(),
            })))
            .Append(')');

        if (!model.HandleMethod.ReturnsTask)
        {
            builder.Append(')');
        }

        builder.AppendLine(";");
        builder.AppendLine("        }");
        builder.AppendLine();

        EmitHelpers(builder, model, responseTypeName, "private", "generated request authoring surface");

        if (model.HasValidation)
        {
            builder.AppendLine();
            EmitValidator(builder, model, requestTypeName);
        }

        builder.AppendLine("    }");
    }

    private static void EmitHandler(StringBuilder builder, RequesterGenerationModel model)
    {
        var requestTypeName = RequesterGeneratorSymbolHelper.GetTypeName(model.ClassSymbol);
        var responseTypeName = RequesterGeneratorSymbolHelper.GetTypeName(model.ResponseType);
        var resultTypeName = $"global::BridgingIT.DevKit.Common.Result<{responseTypeName}>";

        foreach (var attribute in model.PolicyAttributes)
        {
            builder.Append("    ").AppendLine(RequesterGeneratorSymbolHelper.RenderAttribute(attribute));
        }

        builder.AppendLine("    /// <summary>");
        builder.Append("    /// Represents the generated Requester handler for <see cref=\"")
            .Append(requestTypeName)
            .AppendLine("\"/>.");
        builder.AppendLine("    /// </summary>");
        builder.Append("    ").Append(model.AccessibilityKeyword).Append(" class ").Append(model.GeneratedHandlerName)
            .Append(" : global::BridgingIT.DevKit.Common.RequestHandlerBase<")
            .Append(requestTypeName)
            .Append(", ")
            .Append(responseTypeName)
            .AppendLine(">");
        builder.AppendLine("    {");
        builder.AppendLine("        private readonly global::System.IServiceProvider serviceProvider;");
        builder.AppendLine();
        builder.AppendLine("        /// <summary>");
        builder.Append("        /// Initializes a new instance of the <see cref=\"")
            .Append(model.GeneratedHandlerName)
            .AppendLine("\"/> class.");
        builder.AppendLine("        /// </summary>");
        builder.AppendLine("        /// <param name=\"serviceProvider\">The service provider used to resolve dependencies for the generated handler invocation.</param>");
        builder.Append("        public ").Append(model.GeneratedHandlerName).AppendLine("(global::System.IServiceProvider serviceProvider)");
        builder.AppendLine("        {");
        builder.AppendLine("            this.serviceProvider = serviceProvider ?? throw new global::System.ArgumentNullException(nameof(serviceProvider));");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        /// <summary>");
        builder.Append("        /// Forwards the Requester runtime invocation for <see cref=\"")
            .Append(requestTypeName)
            .AppendLine("\"/> into the developer-authored handle method.");
        builder.AppendLine("        /// </summary>");
        builder.AppendLine("        /// <param name=\"request\">The request instance to handle.</param>");
        builder.AppendLine("        /// <param name=\"options\">The request send options.</param>");
        builder.AppendLine("        /// <param name=\"cancellationToken\">The cancellation token for the request.</param>");
        builder.AppendLine("        /// <returns>The result returned by the developer-authored handle method.</returns>");
        builder.Append("        protected override global::System.Threading.Tasks.Task<")
            .Append(resultTypeName)
            .Append("> HandleAsync(")
            .Append(requestTypeName)
            .AppendLine(" request, global::BridgingIT.DevKit.Common.SendOptions options, global::System.Threading.CancellationToken cancellationToken)");
        builder.AppendLine("        {");

        foreach (var parameter in model.HandleMethod.Parameters.Where(static parameter => parameter.Kind == RequesterParameterKind.Service))
        {
            var parameterName = RequesterGeneratorSymbolHelper.EscapeIdentifier(parameter.Symbol.Name);
            builder.Append("            var ")
                .Append(parameterName)
                .Append(" = global::Microsoft.Extensions.DependencyInjection.ServiceProviderServiceExtensions.GetRequiredService<")
                .Append(RequesterGeneratorSymbolHelper.GetTypeName(parameter.Symbol.Type))
                .Append(">(this.serviceProvider);")
                .AppendLine();
        }

        builder.Append("            return ")
            .Append(requestTypeName)
            .Append('.')
            .Append(RequesterGenerationModel.HandleBridgeMethodName)
            .Append("(request, options, cancellationToken");

        foreach (var parameter in model.HandleMethod.Parameters.Where(static parameter => parameter.Kind == RequesterParameterKind.Service))
        {
            builder.Append(", ").Append(RequesterGeneratorSymbolHelper.EscapeIdentifier(parameter.Symbol.Name));
        }

        builder.AppendLine(");");
        builder.AppendLine("        }");
        builder.AppendLine();

        EmitHelpers(builder, model, responseTypeName, "protected", "generated Requester handler");
        builder.AppendLine("    }");
    }

    private static void EmitValidator(StringBuilder builder, RequesterGenerationModel model, string requestTypeName)
    {
        builder.AppendLine("        /// <summary>");
        builder.Append("        /// Provides generated FluentValidation integration for <see cref=\"")
            .Append(requestTypeName)
            .AppendLine("\"/>.");
        builder.AppendLine("        /// </summary>");
        builder.AppendLine("        public sealed class Validator : global::FluentValidation.AbstractValidator<" + requestTypeName + ">");
        builder.AppendLine("        {");
        builder.AppendLine("            /// <summary>");
        builder.Append("            /// Initializes a new instance of the <see cref=\"")
            .Append(requestTypeName)
            .AppendLine(".Validator\"/> class.");
        builder.AppendLine("            /// </summary>");
        builder.AppendLine("            public Validator()");
        builder.AppendLine("            {");
        builder.Append("                var validator = new global::FluentValidation.InlineValidator<")
            .Append(requestTypeName)
            .AppendLine(">();");
        builder.Append("                ").Append(requestTypeName).Append('.').Append(model.ValidateMethod.Name).AppendLine("(validator);");
        builder.AppendLine("                this.Include(validator);");
        builder.AppendLine("            }");
        builder.AppendLine("        }");
    }

    private static void EmitHelpers(
        StringBuilder builder,
        RequesterGenerationModel model,
        string responseTypeName,
        string accessibility,
        string surface)
    {
        if (model.IsUnitResponse)
        {
            EmitHelper(builder, accessibility, "global::BridgingIT.DevKit.Common.Result<global::BridgingIT.DevKit.Common.Unit>", "Success", string.Empty, "global::BridgingIT.DevKit.Common.Result<global::BridgingIT.DevKit.Common.Unit>.Success()", $"Creates a successful unit result for the {surface}.");
        }
        else
        {
            EmitHelper(builder, accessibility, $"global::BridgingIT.DevKit.Common.Result<{responseTypeName}>", "Success", $"{responseTypeName} value", $"global::BridgingIT.DevKit.Common.Result<{responseTypeName}>.Success(value)", $"Creates a successful typed result for the {surface}.");
        }

        EmitHelper(builder, accessibility, $"global::BridgingIT.DevKit.Common.Result<{responseTypeName}>", "Failure", string.Empty, $"global::BridgingIT.DevKit.Common.Result<{responseTypeName}>.Failure()", $"Creates a failed result without any additional message or error for the {surface}.");
        EmitHelper(builder, accessibility, $"global::BridgingIT.DevKit.Common.Result<{responseTypeName}>", "Failure", "string message", $"global::BridgingIT.DevKit.Common.Result<{responseTypeName}>.Failure(message)", $"Creates a failed result with a message for the {surface}.");
        EmitHelper(builder, accessibility, $"global::BridgingIT.DevKit.Common.Result<{responseTypeName}>", "Failure", "global::BridgingIT.DevKit.Common.IResultError error", $"global::BridgingIT.DevKit.Common.Result<{responseTypeName}>.Failure(error)", $"Creates a failed result with a typed error for the {surface}.");
    }

    private static void EmitHelper(
        StringBuilder builder,
        string accessibility,
        string returnType,
        string methodName,
        string parameters,
        string body,
        string summary)
    {
        builder.AppendLine("        /// <summary>");
        builder.Append("        /// ").AppendLine(summary);
        builder.AppendLine("        /// </summary>");

        if (!string.IsNullOrWhiteSpace(parameters))
        {
            foreach (var parameter in parameters.Split(',').Select(static parameter => parameter.Trim()))
            {
                var parameterName = parameter.Split(' ').Last();
                builder.Append("        /// <param name=\"").Append(parameterName).AppendLine("\">The value used to create the generated result.</param>");
            }
        }

        builder.AppendLine("        /// <returns>The generated Requester result.</returns>");
        builder.Append("        ")
            .Append(accessibility)
            .Append(" static ")
            .Append(returnType)
            .Append(' ')
            .Append(methodName)
            .Append('(')
            .Append(parameters)
            .Append(") => ")
            .Append(body)
            .AppendLine(";");
        builder.AppendLine();
    }
}
